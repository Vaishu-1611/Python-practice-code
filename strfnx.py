name ="Vaishnavi"
print(name)
print(len(name)) #no of char in string
print(name.endswith("i")) #boolean gives T/F
print(name.count("a")) #no of specific char in string
print(name.capitalize()) #
print(name.replace("v","V")) #replaces char
print(name.find("h")) #find index of specific char
print(name.lower()) #covert all char in lower case
print(name.upper()) #convert all char in upper case
print(name.title()) #starts string with capital char
print(name.index("s" )) #find index of specific char
print(name.isnumeric()) #boolean gives T/F
print(name.isalpha()) #boolean gives T/F
print(name.split("a")) #splits string from specific char
print(name[::]) #prints string
print(name[::-1]) #reverses string
print(name.startswith("v")) #boolean gives T/F
print(name.replace("a","@")) #replaces char
print('\n') #newline
print(name.swapcase()) #swaps case of all char
