l = [1,2,3,6,3,5,7,8,9,10]
print("Original list:", l)
print(sum(l))  # Sum of all elements
print(max(l))  # Maximum element
print(min(l))  # Minimum element
print(len(l))  # Length of the list
print(sorted(l))  # Sorted list
print(list(reversed(l)))  # Reversed list
print(any(l))  # Check if any element is True
print(all(l))  # Check if all elements are True
print(l.count(5))  # Count occurrences of 5
print(l.index(7))  # Index of element 7
print(l.copy())  # Copy of the list
print(tuple(l))  # Convert list to tuple
print(set(l))  # Convert list to set
